from math import *

my_num = 5
neg_num = -9
print(abs(neg_num))
print(pow(4, 6))  # 4^6
print(max(4, 6))  # prints bigger number
print(round(3.2))  # normal rounding
print(floor(3.7))  # rounds down
print(ceil(3.3))  # rounds up
print(sqrt(my_num))  # prints square root
print(str(my_num) + " my fave number.")  # just like .toString() in java
